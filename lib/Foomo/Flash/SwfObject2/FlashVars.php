namespace Foomo\Flash\SwfObject2;

class FlashVars {
	//---------------------------------------------------------------------------------------------
	// ~ Variables
	//---------------------------------------------------------------------------------------------

	/**
	 * @var array
	 */
	private $vars;

	//---------------------------------------------------------------------------------------------
	// ~ Constructor
	//---------------------------------------------------------------------------------------------

	public function __construct()
	{
		$this->vars = array();
	}

	//---------------------------------------------------------------------------------------------
	// ~ Public methods
	//---------------------------------------------------------------------------------------------

	/**
	 * @param string $name
	 * @param string $vaue
	 */
	public function add($name, $value)
	{
		$this->vars[$name] = $value;
	}

	/**
	 * @return array
	 */
	public function getVars()
	{
		return $this->vars;
	}

}